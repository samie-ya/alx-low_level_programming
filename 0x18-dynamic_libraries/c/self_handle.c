#include "main.h"

/**
* add - function to find the addition result
*
* @a: value used
*
* @b: value used
*
* Return: the added result
*/

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - function to find the subtraction result
*
* @a: value used
*
* @b: value used
*
* Return: the subtracted result
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - function to find the multiplication result
*
* @a: value used
*
* @b: value used
*
* Return: the multiplied result
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - function to find the division result
*
* @a: value used
*
* @b: value used
*
* Return: the divided result
*/

int div(int a, int b)
{
	return (a / b);
}

/**
* mod - function to find remainder
*
* @a: value used
*
* @b: value used
*
* Return: Remainder of the results
*/

int mod(int a, int b)
{
	return (a % b);
}
